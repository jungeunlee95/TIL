package Day2Homework;

//1 영문자 모음의 아스키 코드 값을 아래의 출력형식에 맞추어 화면에 출력하시오

public class Asciicode {
	public static void main(String[] args) {
		//아스키코드 - 컴퓨터 키보드의 문자를 숫자화
		// %d - 정수(10진수) => printf 사용
		
		System.out.printf("A = " + "%d\n", 65);
		System.out.printf("E = " + "%d\n", 69);
		System.out.printf("I = " + "%d\n", 73);
		System.out.printf("O = " + "%d\n", 79);
		System.out.printf("U = " + "%d\n", 85);
		
	
	}
}
